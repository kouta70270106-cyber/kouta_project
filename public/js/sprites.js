// ============================================================
// DQ風ピクセルアートスプライト生成
// HTML Canvas で描画 → Phaser テクスチャとして登録
// BootScene.create() から createGameSprites(scene) を呼ぶ
// ============================================================

const PXSCALE = 3; // 1デザインpx = 3スクリーンpx

function _pb(ctx, gx, gy, gw, gh, color) {
  ctx.fillStyle = color;
  ctx.fillRect(gx * PXSCALE, gy * PXSCALE, gw * PXSCALE, gh * PXSCALE);
}

function _sprite(scene, key, gw, gh, drawFn) {
  const cvs = document.createElement('canvas');
  cvs.width  = gw * PXSCALE;
  cvs.height = gh * PXSCALE;
  drawFn(cvs.getContext('2d'));
  scene.textures.addCanvas(key, cvs);
}

